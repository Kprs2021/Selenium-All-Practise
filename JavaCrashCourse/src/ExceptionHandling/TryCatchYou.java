package ExceptionHandling;

public class TryCatchYou {
	
	/*
	 * Try block: This is a block where we will 
	 * write our code which may give run time exception..
	 * 
	 * Catch Block: This is where the exceptions are handled..
	 * Catch Block is immediately followed by try block.
	 * 
	 * Important Points:
	 * 
	 * A try block can have multiple catch block.
	 * 
	 * If there are multiple catch blocks, they should 
	 * not be of generic exception type.
	 * 
	 * If there are no exceptions then the catch blocks 
	 * will not get executed.
	 * 
	 */
	
	

}
